const os = require('os');

console.log('System Info\n');
console.log('-'.repeat(50));

console.log('Platform Details: ');
console.log('Platform:', os.platform());
console.log('Architecture:', os.arch());
console.log('OS Type:', os.type());
console.log('OS Release:', os.release());
console.log('Hostname:', os.hostname());

console.log('\nCPU Info: ');
const cpus = os.cpus();
// console.log(cpus.length);
console.log('CPU Model:', cpus[0].model);
console.log('Number of cores:', cpus.length);
console.log('CPU Speed:', cpus[0].speed);

console.log('-'.repeat(50));

const totalHotMemory = os.totalmem(); //# RAM
const freeMemory = os.freemem();
console.log(
  'Total Memory:',
  (totalHotMemory / 1024 / 1024 / 1024).toFixed(2),
  'GB'
);
console.log('Free Memory:', (freeMemory / 1024 / 1024 / 1024).toFixed(2), 'GB');

const uptime = os.uptime();
const days = Math.floor(uptime / 86400);
const hours = Math.floor((uptime % 86400) / 3600);
const minutes = Math.floor((uptime % 3600) / 60);
console.log('System Uptime:', `${days} days ${hours} hours ${minutes} minutes`);
